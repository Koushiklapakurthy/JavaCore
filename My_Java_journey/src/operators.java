public class operators {
    public static void main(String[] args){
        //operators
        double a = 5;
        double b = 10;
        int num = 1;
        double add = a+b;
        double sub = a-b;
        double mul = a*b;
        double div = a/b;
        double mod = a%b;
        System.out.println(add);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(mod);
        System.out.println(num++);
        System.out.println(num);
        System.out.println(++num);
        System.out.println(num);
        System.out.println(--num);
        System.out.println(num);
        System.out.println(num--);
        System.out.println(num);


    }
}
